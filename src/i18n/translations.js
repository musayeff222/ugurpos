export function translate(lang, key, vars = {}) {
  const dict = translations[lang] || translations.tr;
  let text = dict[key] ?? translations.tr[key] ?? key;
  Object.entries(vars).forEach(([k, v]) => {
    text = text.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
  });
  return text;
}
